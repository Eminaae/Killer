package predavanjaS04D04;

public class Person {
	public String firstName;
	public String lastName;
	public Integer yearofBirth;
	public String address;
	public String persolnalid;
	public String email;
	public Integer height;
	public boolean isValid;
	public String favFood;
	public String favDrink;
	
	public Person(String firstName, String lastName, Integer yearofBirth,
			String address, String persolnalid, String email, Integer height,
			boolean isValid, String favFood, String favDrink) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
		this.yearofBirth = yearofBirth;
		this.address = address;
		this.persolnalid = persolnalid;
		this.email = email;
		this.height = height;
		this.isValid = isValid;
		this.favFood = favFood;
		this.favDrink = favDrink;
	}
	
	

}
